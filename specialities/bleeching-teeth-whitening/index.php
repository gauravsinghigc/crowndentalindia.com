                            <h3 class="service-details__title">Bleaching / Teeth Whitening</h3>
                            <hr><!-- /.service-details__title -->
                            <p class="service-details__text">If you lack the confidence to smile owing to your yellow or discoloured teeth, bleaching might just be the answer to your problem.<br>

                                Dental bleaching is a simple and very regular cosmetic procedure carried out at <?php echo $name; ?>. This procedure helps get rid of the stains and the yellow texture of teeth. The result is lightening and brightening of the teeth, giving you a younger, more aesthetic and appealing smile.<br><br>

                                <b>1. How is the bleaching procedure carried out?</b><br>

                                Bleaching is carried out using toothpaste like paste which is applied over your teeth for a period of 30 minutes. The paste may be used independently or in conjunction with a light source to activate the agents. The procedure ends with the paste being washed off with water. This procedure is extremely safe and care is taken to make you feel comfortable at the clinic during the procedure.<br><br>

                                <b>2. What are the benefits of Dental Bleaching?</b><br>

                                Bleaching being a cosmetic procedure adds substantial value to your dental aesthetics. The advantage of the bleaching procedure is you having a lighter and younger shade of teeth, that provides you with the confidence to interact with people with a million dollar smile. It may also reveal any problem brewing beneath the stains.<br><br>

                                <b>3. Are there any side effects of bleaching?</b><br>

                                The most common side effect is the sensitivity of teeth for a day after the procedure. This sensitivity only occurs in a few patients. This sensitivity can be warded off using commonly available anti-sensitivity toothpaste.<br><br>

                                <b>4. What is the care to be taken after the bleaching procedure?</b><br><br>

                                <b>The following are the instructions that will be given to you after the bleaching procedure:</b><br>

                                a. Not to have any coloured food items like wine, tea, turmeric and coloured curry/gravy for 3 to 4 days. This is because the teeth tend to absorb excessive colouring agents for a few hours after the procedure.<br>

                                b. Use an anti sensitivity toothpaste for 2 weeks.<br>

                                c. Avoid anything excessively hot or cold for 2 days. This is again related to the side effect of sensitive teeth after the procedure.<br><br>

                                <b>5. Can Bleaching of teeth be done at home?</b><br>

                                Yes, Bleaching of teeth can be done at home. Home bleaching kits are available at the clinic. The doctors will personally train the patients to use this kit.<br>

                                The drawback of home bleaching kits is that they are not as effective in brightening / lightening the shade of teeth as professional bleaching done at the clinic.
